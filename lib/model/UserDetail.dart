import 'package:flutter/foundation.dart';

class UserDetail{

  final String mDay;
  final String mMonth;
  final String mYear;
  final String mEntrada;
  final String mPausa;
  final String mTotal;

  UserDetail({
    @required this.mDay,
    @required this.mMonth,
    @required this.mYear,
    @required this.mEntrada,
    @required this.mPausa,
    @required this.mTotal});
}